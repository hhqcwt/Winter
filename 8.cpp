#include <iostream>
#include <string>
using namespace std;

int main() {
	string song = "Falling in love with you";
	string elvis("Elvis Presley");
	string singer; //문자열 singer

	cout << song + "를 부른 가수는";
	cout << "(힌트:첫글자는 " << elvis[0] << ")?";

	getline(cin, singer); //string타입의 C++문자열을 입력받기위한 전역함수
	if (singer == elvis)
		cout << "맞았습니다";
	else
		cout << "틀렸습니다" + elvis + "입니다" << endl;
}